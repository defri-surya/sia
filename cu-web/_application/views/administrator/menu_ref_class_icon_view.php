    $(document).ready(function () {
        $('.item-fa-class').on('click', function () {
            var str = $(this).find('a').text();
            copy(str);
        });
        
        $('.item-gl-class').on('click', function () {
            var str = $(this).find('span + span').text();
            copy(str);
        });
    });

    function copy(str) {
        const el = document.createElement('textarea');
        el.value = str;
        document.body.appendChild(el);
        el.select();
        document.execCommand('copy');
        alert(str + '\rberhasil tersalin!');
        document.body.removeChild(el);
    }
